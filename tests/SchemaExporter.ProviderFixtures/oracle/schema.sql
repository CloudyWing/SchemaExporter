CREATE TABLE SE_CUSTOMERS (
  ID NUMBER GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  NAME NVARCHAR2(100) NOT NULL,
  EMAIL NVARCHAR2(256) NULL,
  IS_ACTIVE NUMBER(1) DEFAULT 1 NOT NULL,
  CONSTRAINT PK_SE_CUSTOMERS PRIMARY KEY (ID)
);

CREATE TABLE SE_ORDERS (
  ID NUMBER GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  CUSTOMER_ID NUMBER NOT NULL,
  ORDER_NUMBER NVARCHAR2(40) NOT NULL,
  TOTAL_AMOUNT NUMBER(18, 2) NOT NULL,
  CONSTRAINT PK_SE_ORDERS PRIMARY KEY (ID),
  CONSTRAINT FK_SE_ORDERS_SE_CUSTOMERS FOREIGN KEY (CUSTOMER_ID) REFERENCES SE_CUSTOMERS (ID)
);

CREATE UNIQUE INDEX IX_SE_CUSTOMERS_EMAIL ON SE_CUSTOMERS (EMAIL);

COMMENT ON TABLE SE_CUSTOMERS IS 'SchemaExporter fixture customer table';
COMMENT ON COLUMN SE_CUSTOMERS.NAME IS 'Customer display name';

CREATE VIEW SE_ACTIVE_CUSTOMERS
AS
SELECT
  ID,
  NAME,
  EMAIL
FROM SE_CUSTOMERS
WHERE IS_ACTIVE = 1;

CREATE OR REPLACE PROCEDURE SE_GET_CUSTOMERS (
  P_ONLY_ACTIVE IN NUMBER DEFAULT 1
)
AS
BEGIN
  FOR CUSTOMER_ROW IN (
    SELECT ID, NAME, EMAIL
    FROM SE_CUSTOMERS
    WHERE P_ONLY_ACTIVE = 0 OR IS_ACTIVE = 1
  ) LOOP
    NULL;
  END LOOP;
END;
/

CREATE OR REPLACE FUNCTION SE_CUSTOMER_DISPLAY_NAME (
  P_NAME IN NVARCHAR2,
  P_EMAIL IN NVARCHAR2
)
RETURN NVARCHAR2
AS
BEGIN
  RETURN P_NAME || ' <' || P_EMAIL || '>';
END;
